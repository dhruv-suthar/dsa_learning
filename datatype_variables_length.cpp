#include<iostream>
using namespace std;


int main()
{
  //int
  int int_no = 451;
  //float
  float float_no = 451.5;
  //double
  double double_no = 451.555565656;
  //char
  char str = 'C';
  //bool
  bool is_bool = true;

  cout<<"Int datatype value: "<<int_no<<" Size : "<<sizeof(int_no)<<endl;
  cout<<"Float datatype value: "<<float_no<<" Size : "<<sizeof(float_no)<<endl;
  cout<<"Double datatype value: "<<double_no<<" Size : "<<sizeof(double_no)<<endl;
  cout<<"Char datatype value: "<<str<<" Size : "<<sizeof(str)<<endl;
  cout<<"Bool datatype value: "<<is_bool<<" Size : "<<sizeof(is_bool)<<endl;
  

}